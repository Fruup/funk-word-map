	<h2>Info</h2>

	<h3 id="was">Was ist das?</h3>
	<p>
		Hi! Dieses Projekt ist ein Versuch, herauszufinden, in welcher Region Deutschlands welches Wort
		für das Entfernen einer Mandarinenschale verwendet wird. Das Ergebnis wird auf dem
		Instagram-Account <a
			href="https://www.instagram.com/funk/"
			target="_blank"
			rel="noopener noreferrer">@funk</a
		> veröffentlicht.
	</p>

	<h3 id="daten">Welche Daten werden gespeichert und was passiert damit?</h3>
	<p>
		Es wird lediglich die eingegebene Postleitzahl und das eingegebene Wort gespeichert. Diese
		liegen auf Servern innerhalb der EU und sind nicht mit personenbezogenen Daten gekoppelt. Die
		Daten werden völlig anonym zur Erstellung eines Posts des Instagram-Accounts <a
			href="https://www.instagram.com/funk/"
			target="_blank"
			rel="noopener noreferrer">@funk</a
		> genutzt.
	</p>
